
public class StringSinifi {

	public static void main(String[] args) {

		String str = "patika";

		System.out.println(str.charAt(2)); // indisteki değeri verir
		System.out.println(str.concat(".dev")); // ekleme
		System.out.println(str.contains("at")); // içeriyormu
		System.out.println(str.endsWith("ka")); // sonu bitiyormu
		System.out.println(str.equals("patika")); // eşit mi
		
	}

}
